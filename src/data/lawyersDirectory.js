export function getSpecializationsList() {
  return [
    ...new Set(PLATFORM_LAWYERS.map((l) => l.specialization)),
  ].sort((a, b) => a.localeCompare(b, "ar"));
}

export function getLawyerById(id) {
  if (id == null || id === "") return null;
  const key = String(id);
  return PLATFORM_LAWYERS.find((l) => l.id === key) ?? null;
}
